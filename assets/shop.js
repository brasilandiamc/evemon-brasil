    const total = document.getElementById("total");
    const checkout = document.getElementById("checkout");

    function money(v) {
      return "R$" + v.toFixed(2);
    }

    function getFiltered() {
      const q = search.value.trim().toLowerCase();
      return products.filter(p => {
        const category = selected === "Todos" || p.category === selected;
        const query = !q || p.name.toLowerCase().includes(q) || p.desc.toLowerCase().includes(q) || p.category.toLowerCase().includes(q);
        return category && query;
      });
    }

    function renderFilters() {
      const cats = ["Todos", ...order];
      filters.innerHTML = cats.map(cat => {
        const count = cat === "Todos" ? products.length : products.filter(p => p.category === cat).length;
        return `<button class="${selected === cat ? "active" : ""}" onclick="setCategory('${cat}')"><span>${icons[cat]} ${cat}</span><b>${count}</b></button>`;
      }).join("");
      sideTotal.textContent = products.length;
    }

    function setCategory(cat) {
      selected = cat;
      renderFilters();
      renderProducts();
    }

    function renderProducts() {
      const list = getFiltered();
      resultCount.textContent = `${list.length} resultado${list.length === 1 ? "" : "s"}`;
      productArea.innerHTML = order.map(cat => {
        const items = list.filter(p => p.category === cat);
        if (!items.length) return "";
        return `
          <section class="category">
            <div class="category-title">
              <div><h3>${cat}</h3><p>${descriptions[cat]}</p></div>
              <span>${items.length} item${items.length === 1 ? "" : "s"}</span>
            </div>
            <div class="grid">
              ${items.map(p => `
                <article class="product">
                  <span class="badge ${p.badge ? "" : "empty"}">${p.badge || "-"}</span>
                  <div class="image-frame product-image"><img src="${p.image}" alt="${p.name}" loading="lazy"></div>
                  <small>${p.category}</small>
                  <h3>${p.name}</h3>
                  <p>${p.desc}</p>
                  <div class="product-price"><div><span class="value-label">Valor</span><span class="price">${money(p.price)}</span></div></div>
                  <button onclick="addToCart('${p.name}')">Ver detalhes</button>
                </article>
              `).join("")}
            </div>
          </section>
        `;
      }).join("") || `<div class="empty-cart"><strong>Nada encontrado</strong><p>Tente buscar por outro produto.</p></div>`;
    }

    function addToCart(name) {
      const product = products.find(p => p.name === name);
      const found = cart.find(item => item.name === name);
      if (found) found.qty++;
      else cart.push({ ...product, qty: 1 });
      renderCart();
    }

    function removeFromCart(name) {
      cart = cart.filter(item => item.name !== name);
      renderCart();
    }

    function renderCart() {
      const count = cart.reduce((sum, item) => sum + item.qty, 0);
      const value = cart.reduce((sum, item) => sum + item.price * item.qty, 0);

      cartCount.textContent = `${count} item${count === 1 ? "" : "s"}`;
      subtotal.textContent = money(value);
      total.textContent = money(value);
      checkout.disabled = !cart.length;
      checkout.textContent = cart.length ? "Finalizar compra" : "Carrinho vazio";

      cartItems.innerHTML = cart.length ? cart.map(item => `
        <div class="cart-item">
          <div><strong>${item.name}</strong><br><small>${item.qty}x ${money(item.price)}</small></div>
          <button class="remove" onclick="removeFromCart('${item.name}')">Remover</button>
        </div>
      `).join("") : `<div class="empty-cart"><strong>Nenhum item ainda</strong><p>Adicione um produto para ver o total sem sair da página.</p></div>`;
    }

    checkout.addEventListener("click", () => {
      const text = cart.map(i => `${i.qty}x ${i.name}`).join("\n");
      alert(`Pedido Evemon Brasil:\n\n${text}\n\nTotal: ${money(cart.reduce((s,i)=>s+i.price*i.qty,0))}`);
    });

